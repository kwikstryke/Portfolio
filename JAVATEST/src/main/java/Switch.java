public class Switch
{
    public static void main(String[] args)
    {
        int i =44;
        switch (i) {
        case 1:
            System.out.println("jeden");
            break;
        case 2:
            System.out.println("dwa");
            break;
        case 3:
            System.out.println("trzy");
            break;
        default:
            System.out.println("inna wartosc");
    }





    }

}
